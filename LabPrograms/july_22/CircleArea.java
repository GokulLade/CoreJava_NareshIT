/*Suppose the radius of a circle is stored as 3.7cm.Calculate and
   display the area of the circle and circumference of circle.
*/
class CircleArea 
{
	public static void main(String[] args) 
	{
		double radius= 3.7,py=3.14;
		
		System.out.println("Area of Circle is          :"+ (py*radius*radius));
		System.out.println("Circumference of Circle is :"+(2*py*radius));
	}
}
